char	*ft_strcat(char *dest, const char *src)
{
	char *ret;

	ret = dest;
	while (*ret != '\0')
		ret++;
	while (*src != '\0')
	{
		*ret = *src;
		ret++;
		src++;
	}
	*ret = '\0';
	return (dest);
}
