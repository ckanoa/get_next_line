#include "libft.h"

char	*ft_strnstr(char *s1, char *s2, size_t n)
{
	size_t i;
	size_t j;

	i = 0;
	while (s1[i] && i < n)
	{
		j = 0;
		while (s1[i + j] && (i + j) < n && s1[i + j] == s2[j])
			j++;
		if (s2[j] == '\0')
			return ((char*)&s1[i]);
		i++;
	}
	return (NULL);
}
